#include <stdio.h>
/**
 *main - Assign random numbers to variable n
 *Return: - n is positive or negative
 */
int main(void)
{
  int n;
  if (n > 0)
    {
      printf("$n is positive\n");
    }
  else if (n < 0)
    {
      printf("$n is negative\n");
    }
  else
    {
      printf("$n is zero\n");
    }
}
