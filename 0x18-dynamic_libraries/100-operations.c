/**
 * add - sums a and b
 * @a: variable
 * @b: variable
 * Return: sum
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - substracts b from a
 * @a: number
 * @b: number
 * Return: difference
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - miltiplies a and b
 * @a: number used
 * @b: number used
 * Return: product
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - devides a and b
 * @a: no.
 * @b: no.
 * Return: quotient
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - checks remainder
 * @a: number
 * @b: number
 * Return: remainder
 */

int mod(int a, int b)
{
	return (a % b);
}
